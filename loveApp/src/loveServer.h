// read commands:
#define R_DPT           0x0324 //  get decimal points
#define R_PV            0x00   //  read process variable with status
#define R_SP1_VALUE     0x0100 //  set point 1
#define R_SP2_VALUE     0x0102 //  set point 2
#define R_AL_LOW        0x0104 //  Alarm Low Value
#define R_AL_HIGH       0x0105 //  Alarm High Value
#define R_STATUS        0x05   //  Read the full status bit 
